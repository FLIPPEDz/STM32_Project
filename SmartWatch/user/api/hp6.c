/*
函数名:HP6_Init
函数的功能：HP6初始化
参数：无
*/
void HP6_Init(void)
{
	GPIO_InitTypeDef GPIO_HP6_Init;//定义结构体变量
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOC,ENABLE);//打开GPIOC时钟
	GPIO_HP6_Init.GPIO_Pin=GPIO_Pin_13;//设置对应引脚--PC13
	GPIO_HP6_Init.GPIO_Mode=GPIO_Mode_OUT;//设置对应IO口为输出模式
	GPIO_HP6_Init.GPIO_OType=GPIO_OType_PP;//设置对应IO口为推挽模式
	GPIO_HP6_Init.GPIO_PuPd=GPIO_PuPd_NOPULL;//设置对应IO口为浮空模式
	GPIO_HP6_Init.GPIO_Speed=GPIO_Speed_50MHz;//设置对应IO口速度为50MHZ模式
	GPIO_Init(GPIOC,&GPIO_HP6_Init);//初始化GPIOC口
	hp_6_Power(0);
}
/*
函数名:Crc16
函数的功能：HP6初始化
参数：data,要检验的数据 len,要检验数据的长度
返回检验值
*/
uint16_t Crc16(uint8_t *data,uint16_t len)
{
    uint16_t crc16 = 0xFFFF;
    uint32_t uIndex ; //CRC查询表索引
    while (len --)
    {
        uIndex = (crc16&0xff) ^ ((*data) & 0xff) ; //计算CRC
				data = data + 1;
        crc16 = ((crc16>>8) & 0xff) ^ crc16_tab[uIndex];
    }
    return crc16 ;//返回CRC校验值
}

void HP6_IIC_Write(uint8_t *data)
{
	u8 i=0;
	IIC_Start(0);//0-PA,1-PB
	IIC_SendByte(0,hp6_addr<<1);//0-PA,1-PB
	for(i=0;i<buff_lenth;i++)
	{
		IIC_SendByte(0,*(data+i));//0-PA,1-PB
	}
	IIC_Stop(0);//0-PA,1-PB
}
void HP6_IIC_Read(uint8_t *data)
{
	u8 i=0;
	IIC_Start(0);
	IIC_SendByte(0,(hp6_addr<<1)|0x01);
	for(i=0;i<23;i++)
	{
		*(data+i)=IIC_ReadByte(0,0);
	}
	*(data+23)=IIC_ReadByte(0,1);//第23位数据无需应答
	IIC_Stop(0);
}

