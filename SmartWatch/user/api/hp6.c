//发送命令到从机并获取
//返回1 校验成功,返回0 校验失败
uint8_t HP6_Send_Get(uint8_t *HP6_Send_buff,uint8_t *HP6_Result_buff)
{
	uint16_t crc;
	crc = Crc16(&HP6_Send_buff[4],18); //数据校验
  *(uint16_t*)(&HP6_Send_buff[22]) = crc;
	HP6_IIC_Write(HP6_Send_buff); //发送命令
	Delay_nms(5); //读写间隔延时
	HP6_IIC_Read(HP6_Result_buff); //读取返回值
	crc = *(uint16_t*)(&HP6_Result_buff[22]); //校验确定都回来的是否为有效数据
  if(crc==Crc16(&HP6_Result_buff[4],18))//校验成功
	return 1;
	else
	return 0;
}
